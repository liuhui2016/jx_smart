package com.game.util;

public enum JumpType {
    /** 跳转类型：推荐 RECOMMEND_TYPE(1) */
    TYPE_RECOMMEND(1),
    /** 跳转类型：关键字 KEYWORD_TYPE(2) */
    TYPE_HOTWORD(2),
    /** 跳转类型：友情链接 TYPE_FRIENDLY_LINK(3) */
    TYPE_FRIENDLY_LINK(3);

    JumpType(int jtype) {
        this.jtype = jtype;
    }

    private int jtype;

    public int getJtype() {
        return jtype;
    }

    public void setJtype(int jtype) {
        this.jtype = jtype;
    }

}
