package com.game.smvc.entity;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "jx_table_video")
public class JxTableVideo {

	private int v_id;
	private String v_name;
	private String v_url;
	private String v_dir;
	private int v_type;
	private Date v_addtime;
	private Date v_modtime;
	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	public int getV_id() {
		return v_id;
	}
	public void setV_id(int v_id) {
		this.v_id = v_id;
	}
	public String getV_name() {
		return v_name;
	}
	public void setV_name(String v_name) {
		this.v_name = v_name;
	}
	public String getV_url() {
		return v_url;
	}
	public void setV_url(String v_url) {
		this.v_url = v_url;
	}
	public String getV_dir() {
		return v_dir;
	}
	public void setV_dir(String v_dir) {
		this.v_dir = v_dir;
	}
	public int getV_type() {
		return v_type;
	}
	public void setV_type(int v_type) {
		this.v_type = v_type;
	}
	public Date getV_addtime() {
		return v_addtime;
	}
	public void setV_addtime(Date v_addtime) {
		this.v_addtime = v_addtime;
	}
	public Date getV_modtime() {
		return v_modtime;
	}
	public void setV_modtime(Date v_modtime) {
		this.v_modtime = v_modtime;
	}
	
	
}
